import math

pi = 3.1415926
e = 2.7181
g = 1.61

print(abs(pi))
#absolute value

print(round(pi))
#rounds the number

print(math.ceil(pi))
#ceiling function

print(pow(pi, 2))
#raise to the 2nd power

print(math.cbrt(pi))
#cubic root root

print(max(pi, e, g))
#prints the max value

print(min(pi, e, g))
#prints the min value

